        {
        // pharmacie        
        menuGauche.removeAll();
        menuGauche.repaint();
        menuGauche.revalidate();

        menuGauche.add(menu2Pharmacie);
         menuGauche.repaint();
        menuGauche.revalidate();
        }
        if (jTextField1.getText().equals("pa"))
        {
            //patient
         menuGauche.removeAll();
        menuGauche.repaint();
        menuGauche.revalidate();

        menuGauche.add(menu3Patient);
         menuGauche.repaint();
        menuGauche.revalidate();
        }
        if (jTextField1.getText().equals("a"))
        {
        //administrateur
         menuGauche.removeAll();
        menuGauche.repaint();
        menuGauche.revalidate();

        menuGauche.add(menu4Administrateur);
         menuGauche.repaint();
        menuGauche.revalidate();
        }
        
  
        
